#include <iostream>
using namespace std;

int main()
{
    string name; cout<< "Employee Name: "; getline(cin, name);
    int gross; cout<< "Gross Salary: "; cin>> gross;
    int installment; cout<< "Installment: "; cin>> installment;
    int insurance; cout<< "Insurance: "; cin>> insurance;

    int tax= gross*0.2;
    int net= gross-tax-installment-insurance;

    cout<<"Payslip for Employee\n";
    cout<<"---------------------\n";

    cout<< "Name: "<< name << endl;
    cout<< "Gross Salary: Rp"<< gross << endl;
    cout<< "Tax (20%): Rp"<< tax << endl;
    cout<< "Installment: Rp"<< installment<< endl;
    cout<< "Insurance: Rp"<< insurance<< endl;
    cout<< "Net Salary: Rp"<< net<< endl;;
}
